-- tipos de tablas

-- catálogo
-- los elementos que se van a mostrar en la app , tabla que se modifica poco 


-- operación

--  registramos todo lo que nuestro negocio va haciendo como ventas



-- registro
--   es casi como la de operacion , para datos que cambian
-- por ejemplo el precio de venta de una botella de agua puede cambiar constantemente


-- archivo 
-- es dejar almacenado todas las ejecuciones con permisos especiales
-- que tiene un usuario. pierde normalizacion pero  facilita datos historicos suaves 

